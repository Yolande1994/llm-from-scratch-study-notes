for epoch in range(epochs):
    optimizer.zero_grad()
    logits, _, _ = model(train_x)      # 前向传播
    loss = criterion(logits, train_y)  # 计算损失
    loss.backward()   # 反向传播
    optimizer.step()  # 权重更新
    # 打印日志
    if (epoch + 1) % 20 == 0:
        pred = torch.argmax(logits, dim=1)
        acc = (pred == train_y).sum().item() / len(train_y)  # 计算准确率
